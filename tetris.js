function drawSquare(x, y, color) {
    context.fillStyle = color;
    context.fillRect(x * SQ, y * SQ, SQ, SQ);
    context.strokeStyle = "WHITE";
    context.strokeRect(x * SQ, y * SQ, SQ, SQ);
}

